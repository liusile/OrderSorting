        private void btnPrint_Click(object sender, EventArgs e)
        {
            try
            {
                var orderIdArray = txtOrderid.Text.Split(Environment.NewLine.ToArray());
                foreach (var orderId in orderIdArray)
                {
                    if (string.IsNullOrWhiteSpace(orderId))
                        continue;
                    var msg = string.Empty;
                    var Orderinfo = OrderSortService.GetOrderInfoById(orderId, UserInfo, ref msg);
                    if (!string.IsNullOrWhiteSpace(msg))
                    {
                        throw new Exception(msg);
                    }
                    if (Orderinfo != null)
                    {
                        new TestPrintDocument().PrintSetup(Orderinfo);
                    }
                }
            }
            catch (Exception ex)
            {
                MessageBox.Show(ex.Message);
            }
        }

